from math import sin,cos,tan, radians
angulo = float(input('Digite um ângulo:'))
so = sin(radians(angulo))
co = cos(radians(angulo))
ta = tan(radians(angulo))
print(f'O ângulo de {angulo:.2f} tem \nO Seno de {so:.2f} \nO Cosseno de {co:.2f} \nE a tangente de {ta:.2f}')

#Sin = seno
#Co = Cosseno
#Ta = Tangente
#Radians = transforma o calculo do ângulo em radianos